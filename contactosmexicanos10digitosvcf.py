archivoOriginal = open('sample_data/dummy-contacts.vcf')
contactos = [x for x in archivoOriginal.readlines()]

"""## Se modifica la data:"""

contactos = [x.replace('+521','') if x[:3]=='TEL' else x for x in contactos]
contactos = [x.replace('+52 1','') if x[:3]=='TEL' else x for x in contactos]

"""## Se Guarda la informacion"""

with open('directorioResultado.vcf','w') as archivoFinal:
  archivoFinal.writelines(contactos)